                <h2>フランク・オーシャンとは?<br>
謎に包まれた天才のこれまでと、その音楽性</h2>
<hr>
    <h3>Frank Ocean (フランク・オーシャン) : Christopher Lonny Breaux </h3>
<p>
プライベートは一切謎に包まれているアーティスト、フランク・オーシャン。<br>
グラミー賞にすら、『Blonde』以来、参加しないことを表明している。<br>
この音楽家は社会とのコンタクトなしに、なぜ天才と賞賛されるのか。<br>
人前にあまり登場しないにも関わらず、彼の名前や記事は、毎日のように目にするのです。<br>
そんな彼の過去や、音楽性について時系列順に紹介します。</p>
<h5>「ニューオリンズ」</h5>
<br>
<img src="/images/2.jpeg" class="news1_1">
<br>
<p>1987年10月28日生まれ、現在31歳のフランク・オーシャンは５歳の頃カリフォルニアからルイジアナ州ニューオリンズ (アメリカ南部) に家族で移り住みます。<br>
ニューオリンズといえば、ニューオリンズ・ジャズで有名な場所ですが、フランクも母親のカーステレオでよくジャズを聞いていました。彼の音楽のベースにはジャズがあるのかもしれません。<br>
そんなジャズ好きのお母様、Katonya Breaux (カトーニャ・ブロー) は曲中にも時々登場するのですが、いわゆるシングルマザーで、フランクにとっては祖父が父親代わりだったと言われています。<br>
<br>
そんな事情もあったブロー一家ですが、フランクは大学に進学することができます。<br>
地元・ニューオリンズ大学に入学し音楽を勉強しながら、当時からアルバイトで稼いだお金でスタジオを借りて音楽制作を行なっていました</p><br>


<h5>「カトリーナ」</h5><br>
<img src="/images/3.png"class="news1_2">
<p>
地元のニューオリンズ大に進学したフランクでしたが、2005年ニューオリンズを襲ったハリケーン ” カトリーナ ” によって彼の住む家とスタジオは崩壊してしまいます。<br>
この出来事は彼の音楽にも大きな影響を与えていて、リリックにはこの出来事に関係するものが度々表れます。<br>

スタジオを失ったフランクは、LAに移り音楽制作を始めます。当初は6週間だけのつもりでしたが彼の才能を見抜いた業界の人間との繋がりもでき、LAに留まり音楽を続けることを決断します。<br>

LAに残って彼がまず始めたことはビートを作ったり、音楽をリリースすることではなく、作詞・ソングライターとしての仕事でした。ジャスティン・ビーバーやジョン・レジェンド、ビヨンセ、ブランディなどに歌詞を提供するなどライティングで頭角を表します。<br>
</p><br>


<h5>「Odd Future (オッド・フューチャー)」</h5><br>
<img src="/images/4.png" class="news1_3">
<p>
「人に曲を書き続けることが自分が故郷を離れた理由ではないんだって思った、その頃は本当に暗黒時代だったよ、スターに曲を書いて何百万ドルももらったけど、なんだか哀れで。同じようなマインドを持ってる集まりのOdd Futureはクルーだけど、みんな”自分でやるんだ”ってメンタリティがあって、そこに惹かれたんだ」<br>
<br>

と語るフランクは、タイラー・ザ・クリエイターやアール・スウェットシャツなどのメンバーにLAで出会い、自らの音楽制作に加え、グループとしても作曲を本格的にスタートさせます。<br>
この時期に制作した楽曲がミックステープ 『The Lonny Breaux Collection』に収録されています<br><br>
</p>

<h5>「MixTape & ” Car ” (ミックステープと「車」」</h5>
<img src="/images/5.png"class="news1_4"><img src="/images/6.png" class="news1_5">

<p>
2011年、64曲に及ぶミックステープ『The Lonny Breaux Collection』を、同年にミックステープ 『nostalgia, ultra』をリリース。<br>
    後者のテープでは、フランクの少年時代や、ハリケーンに襲われたりした彼のそれまでの人生について “ノスタルジック “がテーマに語られます。<br><br>

昔のことを思い返す時、季節の変わり目の匂いなんかに、ふと情緒的に、いわゆる” エモく”なる感覚がありますよね。”ノスタルジック”という感覚はフランク・オーシャンの音楽を表す一つのワードだと思っていて、彼の音楽は、たとえ歌詞や曲の背景がわからなくても、メロディを聞くだけで、風景が思い起こされたり、彼の感情が伝わってきたり、昔の記憶が頭にふと出てきたりするんです。<br>
そんなメロディ、音楽を作り出せることが彼の凄いところで、誰よりも優れていることだと思います。<br>
彼が楽曲に込めた感情が、音を通して、それを聞いた者のそれぞれの思い出や感情を想起させるのです。表現者として最も難しいことで、最も素晴らしいことだと思います。<br><br>

そんなミックステープ 『nostalgia, ultra』はRadio HeadやMGMT、Eaglesなどのロックバンドをサンプルを多く用いたR&Bアルバムに仕上がり、かなりの話題になります。<br>
そしてこの作品が評価されたことによって、2011年4月Def Jamとの契約を交わします。<br><br>

その後、グラミー年間ベストヒップホップアルバムに選ばれた” Kanye Wesy & JAY-Zの『Watch The Throne』に2曲客演として参加したり、タイラーのデビューアルバム『Goblin』にも参加してメキメキとその知名度を上げていきます。<br><br>

— この2つのミックステープのジャケットには共通して「 車 」が用いられています。<br>
『The Lonny Breaux Collection』の一曲目のタイトルは “Acura Integurl “。これはジャケットのブラックの車の名称で、家族が1998年に買った” Honda Acura “であると Nights – Frank Ocean のリリックでも語られています。<br><br>

『notalgia, ultra』のジャケットに写るオレンジの車は、BMW E30 M3で、彼がずっと憧れだった車だと言われています。彼の車への愛はジャケットだけでなく、リリックにも度々登場したり、曲のタイトルにもなっていたりします。<br><br>

ヒップホップ・ラップの世界で「 車 」というと、ステータスを表すモノのような風潮がありますが、フランクの場合はストーリーテリングにおいての表現の一つとして用いています。<br>
彼の音楽・リリックは、まるで孤独なドライブのような、非常にプライベートなものであったり、内省的なものが多く、その時々の記憶や感情のフレームとして車を用いているんですね。<br><br>
</p>
